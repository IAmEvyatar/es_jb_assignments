/*

	100 = 1100100
	127 = 1111111
	1100010
		     &&
	1111111
	_______
	1100010
	=
	100

*/
console.log(k);
k = 100 | 127;
/*

	100 = 1100100
	127 = 1111111
	1100100
		     ||
	1111111
	_______
	1111111
	=
	127

*/
console.log(k);


